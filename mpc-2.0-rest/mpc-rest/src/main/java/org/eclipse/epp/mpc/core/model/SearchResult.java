package org.eclipse.epp.mpc.core.model;

import java.net.URL;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlType;

/**
 * Search results returned by the marketplace server through the {@link MarketplaceService}. The actual number of
 * returned nodes might be less than the number of matches on the server. In that case, the {@link #getCount()
 * match count} reflects the number of actual matches and the {@link #getNodes() returned results} are a subset of all
 * matches.
 *
 * @author David Green
 * @author Carsten Reckord
 * @noextend This class is not intended to be extended by clients.
 * @noimplement This class is not intended to be implemented by clients.
 */
@XmlAccessorType(XmlAccessType.FIELD)
@XmlType
public class SearchResult extends NodeList {

	@XmlAttribute
	public String term;
	
	@XmlAttribute
	public URL url;
}